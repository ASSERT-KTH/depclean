package neverused;

public class UntestedClass {

    int a;
    int b;

    public UntestedClass(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int sum() {
        return a + b;
    }

    public int sub() {
        return a - b;
    }

    public int mul() {
        return a * b;
    }

}
